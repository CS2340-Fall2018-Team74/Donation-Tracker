package edu.gatech.donationtracker.controller;

import android.support.v4.app.FragmentActivity;
import android.os.Bundle;


import android.view.View;
import android.widget.Toast;
import android.support.v4.app.ActivityCompat;

import com.google.android.gms.maps.CameraUpdateFactory;
import com.google.android.gms.maps.GoogleMap;
import com.google.android.gms.maps.MapView;
import com.google.android.gms.maps.OnMapReadyCallback;
import com.google.android.gms.maps.SupportMapFragment;
import com.google.android.gms.maps.model.LatLng;
import com.google.android.gms.maps.model.Marker;
import com.google.android.gms.maps.model.MarkerOptions;
import com.google.android.gms.tasks.OnCompleteListener;
import com.google.android.gms.tasks.Task;

import java.util.List;
import java.util.Map;

import edu.gatech.donationtracker.R;
import edu.gatech.donationtracker.model.Locations;
import edu.gatech.donationtracker.model.Model;

public class MapsActivity extends FragmentActivity implements GoogleMap.OnMarkerClickListener,
        OnMapReadyCallback {
    private GoogleMap mMap;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_maps);
        SupportMapFragment mapFragment =
                (SupportMapFragment) getSupportFragmentManager().findFragmentById(R.id.map);
        mapFragment.getMapAsync(this);
    }

    /**
     * Called when the map is ready.
     */
    @Override
    public void onMapReady(GoogleMap map) {
        mMap = map;
//        enableMyLocation();
        Map<String, Locations> locations = getLocations();
        for (String name : locations.keySet()) {
            map.addMarker(new MarkerOptions()
                    .position(new LatLng(Double.parseDouble(locations.get(name).getLatitude()), Double.parseDouble(locations.get(name).getLongitude())))
                    .title(name + " " + locations.get(name).getPhone()));
        }
        // Set a listener for marker click.
        mMap.setOnMarkerClickListener(this);
    }


    /**
     * Called when the user clicks a marker.
     */
    @Override
    public boolean onMarkerClick(final Marker marker) {

        // Retrieve the data from the marker.
        Integer clickCount = (Integer) marker.getTag();

        // Check if a click count was set, then display the click count.

        if (clickCount != null) {
            clickCount = clickCount + 1;
            marker.setTag(clickCount);

            Toast.makeText(this,
                    marker.getTitle() +
                            " has been clicked " + clickCount + " times.",
                    Toast.LENGTH_SHORT).show();
        }

        // Return false to indicate that we have not consumed the event and that we wish
        // for the default behavior to occur (which is for the camera to move such that the
        // marker is centered and for the marker's info window to open, if it has one).
        return false;
    }

    /**
     * get all locations latitude and longitude as a map
     * @return map of all locations' latitude and longitude
     */
    public Map<String, Locations> getLocations() {
        return Model.getLongtitudeLatitude();
    }
}
